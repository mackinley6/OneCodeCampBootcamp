// What is DOM?
// DOM or Document Object Model.  is a programming interface for web documents. It represents the structure of a document as a tree of objects, where each object corresponds to a part of the document


// What is node?
// Document node is at the top of the hierarchical structure that represents the entire document.
// Element Nodes: Represent HTML or XML elements in the document.
// Attribute Nodes: Represent attributes of elements.
// Text Nodes: Represent text within elements.
// Comment Nodes: Represent comments in the document.

// What is it use for?
// The Document Object Model (DOM) is used for representing and interacting with the structure and content of documents, primarily HTML and XML documents in the context of web development.



